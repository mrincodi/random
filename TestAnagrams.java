	public static void putAnagramsTogether ( String [] array ) {

		if ( array == null ) return;	// Or throw an exception.

		java.util.HashMap <String, java.util.ArrayList <String> > h = new java.util.HashMap <String, java.util.ArrayList <String> > ();

		for ( int i = 0; i < array.length; i++ ){
			if ( array [ i ] != null ) {
				String sortedLetters = sortLetters ( array [ i ] );

				java.util.ArrayList <String> currentList = h.get ( sortedLetters  ) ;

				if ( currentList == null )
					currentList= new java.util.ArrayList <String> ();

				currentList.add ( array [ i ]  );

				h.put ( sortedLetters,currentList);
			}
		}

		//Now, print the results:

		for ( String key: h.keySet() ){
			java.util.ArrayList <String> currentList = h.get ( key ) ;
			System.out.print ( currentList.size() + " ");
			for ( int j = 0; j < currentList.size(); j++ ){
				System.out.print ( currentList.get ( j ) + " ");
			}
			System.out.println();
		}
	}

	public static String sortLetters ( String s ){

		char [] cA = s.toCharArray ( );
		java.util.Arrays.sort ( cA );
		String sorted = new String ( cA );
		return sorted;
	}


